public class ReutilizandoVariaveis {
    public static void main(String[] args) {

        int salario = 1000;
        double resultado = salario * 0.3;
        System.out.println("O salário é: " + salario + ", 30% é: " + resultado);
        resultado = salario * 0.15;
        System.out.println(" a porcentagem de 15 é: " + resultado);
        resultado = salario * 0.05;
        System.out.println(" a porcentagem de 5 é: " + resultado);
    }
}
